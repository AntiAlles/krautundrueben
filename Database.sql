ALTER TABLE EAKATEGORIENAME ADD PRIMARY KEY (EID);
ALTER TABLE LIEFERANT ADD PRIMARY KEY (LIEFERANTENNR);
ALTER TABLE ORT ADD PRIMARY KEY (PLZ);


DROP DATABASE IF EXISTS krautundrueben;
CREATE DATABASE IF NOT EXISTS krautundrueben;
USE krautundrueben;


CREATE TABLE KUNDE (
    KUNDENNR        INTEGER AUTO_INCREMENT NOT NULL,
    NACHNAME        VARCHAR(50),
    VORNAME         VARCHAR(50),
    GEBURTSDATUM    DATE,
	STRASSE         VARCHAR(50),
	HAUSNR		    VARCHAR(6),
    TELEFON         VARCHAR(25),
    EMAIL           VARCHAR(50),
    PLZ             VARCHAR(5),
    PRIMARY KEY (KUNDENNR)
    );

CREATE TABLE BESTELLUNG(
    BESTELLNR       INTEGER AUTO_INCREMENT  NOT NULL,
    KUNDENNR        INTEGER,
    DATUM           DATE,
    NETTOPREIS      DECIMAL(10,2),
    PRIMARY KEY (BESTELLNR)
);

CREATE TABLE ZUTATEN (
    ZID             INTEGER AUTO_INCREMENT NOT NULL,
    ZUTAT           VARCHAR(50),
    MENGENEINHEIT   VARCHAR(25),
    KALORIEN        INTEGER,
    BESTAND         INTEGER,
    NETTOPREIS      DECIMAL(10,2),
    FETT            DECIMAL(10,2),
    KOHLENHYDRATE   DECIMAL(10,2),
    PRIMARY KEY (ZID)
);

CREATE TABLE ALLERGENE (
    AID             VARCHAR(1),
    BESCHREIBUNG    VARCHAR(500),
    ALLERGEN     	VARCHAR(25),
    PRIMARY KEY (AID)
);

CREATE TABLE ENKATEGORIE (
    EID             INTEGER NOT NULL,
    BESCHREIBUNG    VARCHAR(500),
    EAKATEGORIENAME		VARCHAR(25),
    PRIMARY KEY (EID)
);

CREATE TABLE LIEFERANT (
    LIEFERANTENNR   INTEGER AUTO_INCREMENT NOT NULL,
    STRASSE         VARCHAR(50),
    HAUSNR          VARCHAR(6),
    LIEFERANT       VARCHAR(25),
    PLZ             VARCHAR(5),
    PRIMARY KEY (LIEFERANTENNR)
);

CREATE TABLE ORT (
    PLZ             VARCHAR(5),
    STADT           VARCHAR(50),
    PRIMARY KEY (PLZ)
);

CREATE TABLE REZEPTE (
    RID             INTEGER AUTO_INCREMENT NOT NULL,
    REZEPTE         VARCHAR(25),
    BESCHREIBUNG    VARCHAR(500),
    PRIMARY KEY (RID)
);

CREATE TABLE BESTELLUNG_ZUTAT (
    BESTELLNR       INTEGER,
    ZID             INTEGER,
    MENGE           INTEGER
);

CREATE TABLE ZUTAT_ALLERGENE (
    AID             VARCHAR(1),
    ZID             INTEGER
);

CREATE TABLE ZUTAT_EKG (
    EID             INTEGER,
    ZID             INTEGER
);

CREATE TABLE REZEPT_ZUTAT (
    RID             INTEGER,
    ZID             INTEGER,
    MENGE           INTEGER
);

CREATE TABLE LIEFERANT_ZUTAT ( 
    LIEFERANTENNR       INTEGER,
    ZID                 INTEGER
);

